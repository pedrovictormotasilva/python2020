import math
an = float(input('Digite o ângulo que você deseja'))
seno = math.sin(math.radians(an))
print('O ângulo {} tem o valor de SENO {:.2f}'.format(an, seno,))
cos = math.cos(math.radians(an))
print('O ângulo {} tem o valor de COSSENO {:.2f}'.format(an, cos))
tan = math.tan(math.radians(an))
print('O ângulo {} tem o valor da TANGENTE {:.2f}'.format(an, tan))








